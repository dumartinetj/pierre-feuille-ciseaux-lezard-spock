<p>
	Si vous n'avez pas tous suivis voici un récapitulatif de la règle du jeu accompagnée d'un schéma explicatif.
</p>

<p>	
	<ul> 
		<li> La pierre écrase le lézard </li> 
		<li> Le lézard empoisonne Spock </li> 
		<li> Spock casse les ciseaux </li> 
		<li> Les ciseaux décapitent le lézard </li>
		<li> Le lézard mange la feuille </li> 
		<li> La feuille désavoue Spock </li> 
		<li> Spock vaporise la pierre </li> 
	</ul>
</p>
<p>
	Pour démarrer une partie vous devais indiquer le nombre de manches que vous voulez jouer, ce nombre doit être
	impair pour qu'une égalité ne soit pas possible. De plus, le nombre de manches maximum est de 9 pour éviter que les parties soient trop longues.
	Pour gagner une partie il faut remporter assez de manche pour que l'adversaire, même s'il gagne toutes les manches restantes ne puisse pas remporter la victoire.
	Par exemple si vous jouez une partie en cinq manches et que vous remportez les trois premières, vous avez gagné la partie et vous ne jouerez donc pas les manches restantes votre victoire étant déjà assurée.
	
